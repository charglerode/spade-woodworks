exports.getUser = catchAsync(async (req, res, next) => {
  const user = await User.findById(req.params.id);

  if (!user) {
    return next(new AppError(`No user found with id <${req.params.id}>`), 404);
  }

  res.status(200).json({
    status: 'success',
    data: {
      user,
    },
  });
});

